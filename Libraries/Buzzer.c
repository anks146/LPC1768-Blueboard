#include<lpc17xx.h>
#include "Buzzer.h"
#include "Timer.h"

//=========================================================================================
//================================== BUZZER START==========================================
void Buzzer_Start(void)
{
		LPC_GPIO2->FIOCLR|=(1<<11);
}

//=========================================================================================
//================================== BUZZER STOP===========================================
void Buzzer_Stop(void)
{
		LPC_GPIO2->FIOSET|=(1<<11);
}
//=========================================================================================
//================================== BUZZER INITIALIZE=====================================
void Buzzer_Init(void)     
{
	LPC_PINCON->PINSEL4&=~(3<<20);		// Initialize pin for Buzzer
	LPC_GPIO2->FIODIR|=(1<<11);				// Set buzzer pin as output
	Buzzer_Stop();
}
//=========================================================================================	
//================================== BUZZER SOUND==========================================

void Buzzer_Sound(int Buzzer_Count)
{
	int Buzzer_Loop_Count;
	for(Buzzer_Loop_Count=0;Buzzer_Loop_Count<Buzzer_Count;Buzzer_Loop_Count++)
	{
		Buzzer_Start();
		Delay(125);
		Buzzer_Stop();
		Delay(125);
	}
}
